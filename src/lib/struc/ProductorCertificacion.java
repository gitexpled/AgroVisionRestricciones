package lib.struc;

public class ProductorCertificacion {

	private int idCert;
	private String codProductor;
	private int idCertificacion;
	private String vigencia;
	private String certificado;
	
	public int getIdCert() {
		return idCert;
	}
	public void setIdCert(int idCert) {
		this.idCert = idCert;
	}
	public String getCodProductor() {
		return codProductor;
	}
	public void setCodProductor(String codProductor) {
		this.codProductor = codProductor;
	}
	public int getIdCertificacion() {
		return idCertificacion;
	}
	public void setIdCertificacion(int idCertificacion) {
		this.idCertificacion = idCertificacion;
	}
	public String getVigencia() {
		return vigencia;
	}
	public void setVigencia(String vigencia) {
		this.vigencia = vigencia;
	}
	public String getCertificado() {
		return certificado;
	}
	public void setCertificado(String certificado) {
		this.certificado = certificado;
	}
	
	
}
