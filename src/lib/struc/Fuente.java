package lib.struc;

public class Fuente {

	private int idFuente;
	private String nombre;
	
	public int getIdFuente() {
		return idFuente;
	}
	public void setIdFuente(int idFuente) {
		this.idFuente = idFuente;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
}
